# --------------------------------------------
# Operadores y Comparadores en Python
# --------------------------------------------
# Operadores más comunes: aritméticos, comparadores y lógicos.

# --------------------------------------------
# 1. Operadores aritméticos
# --------------------------------------------
a = 10
b = 3

print("Operadores Aritméticos:")
print(a + b)  # Suma
print(a - b)  # Resta
print(a * b)  # Multiplicación
print(a / b)  # División
print(a // b)  # Division entera
print(a % b)  # Módulo
print(a**b)  # Potencia

# --------------------------------------------
# 2. Operadores comparadores
# --------------------------------------------
print("Operadores Comparadores:")
print(a == b)  # Igualdad: False
print(a != b)  # Desigualdad: True
print(a > b)  # Mayor que: True
print(a < b)  # Menor que: False
print(a >= 5)  # Mayor o igual: True
print(b <= 10)  # menor o igual: True

# --------------------------------------------
# 3. Operadores lógicos
# --------------------------------------------
verdadero = True
falso = False
print("Operadores Lógicos:")
print(verdadero and falso)  # AND lógico: False
print(verdadero or falso)  # OR lógico: True
print(not verdadero)  # NOT lógico: False
